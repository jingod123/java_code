package edu.variable;

public class Ex01 {
	
	public static void main(String[] args) {
		//정수형 변수 num 선언하기
		int num; //정수형 값이 저장될 수 있는 공간 할당, 초기값을 넣지 않아도 됨
		
		//선언된 변수 num에 값 20 할당하기
		num = 20;
		
		//정수형 변수 num 선언하기
		//int num; 같은 이름의 변수 선언 불가
		
		//정수형 상수 num2 선언하기
		final int num2;
		
		//선언된 상수 num2에 값 30 할당하기
		num2 = 30;
		
		//+
		//정수형 변수 num3를 선언하고 50 할당하기
		int num3 = 50;
		//상수 num4를 선언하고 60 할당하기
		final int num4 = 60;
		//선언된 num4에 값 70할당하기
		//num4 = 70; 상수 값을 변경 불가능
		//** 일반적으로 상수는 변수와 구분짓기 위해 대문자를 사용함
		final int NUM = 100;
		
	}
	
}
